def solution(total_lambs):
    t = total_lambs
    a = b = []
    i = j = 1
    while i < t / i:
        a.append(i)
        i = i + 1
    while j < t / j+1:
        b.append(j)
        j = j + 1
    x = len(a)
    y = len(b)
    print(a)
    print(x)
    print(b)
    print(y)
solution(int(input()))